import { DEFAULT_TERMINAL_SCROLLBACK } from './terminal-settings';

const DEFAULT_TERMINAL_FONT_FAMILY = "Consolas, 'Courier New', monospace";

/** @deprecated 使用 resolveTerminalRuntimeSettings().scrollback / DEFAULT_TERMINAL_SCROLLBACK */
export const XTERM_SCROLLBACK_LINES = DEFAULT_TERMINAL_SCROLLBACK;
export const MIN_TERMINAL_FONT_SIZE = 10;
export const MAX_TERMINAL_FONT_SIZE = 24;

/** Clamps user terminal font size settings to supported xterm bounds. */
export function clampTerminalFontSize(value: number | undefined): number {
  if (!Number.isFinite(value)) {
    return 14;
  }

  return Math.min(
    Math.max(Math.round(value ?? 14), MIN_TERMINAL_FONT_SIZE),
    MAX_TERMINAL_FONT_SIZE,
  );
}

/** Returns a usable xterm font-family value. */
export function getTerminalFontFamily(value: string | undefined): string {
  const normalized = value?.trim();
  return normalized || DEFAULT_TERMINAL_FONT_FAMILY;
}

